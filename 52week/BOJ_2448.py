N = int(input())

stars = [[" " for _ in range(5*N//3 + N//3)]for _ in range(N)]


# x, y는 별의 꼭대기, n은 재귀의 depth를 의미
def func(x, y, n):
    if n == 1:
        stars[x][y] = "*"
        stars[x+1][y-1] = "*"
        stars[x+1][y+1] = "*"
        stars[x+2][y-2] = "*"
        stars[x+2][y-1] = "*"
        stars[x+2][y] = "*"
        stars[x+2][y+1] = "*"
        stars[x+2][y+2] = "*"
        
    else:
        m = n//2
        func(x, y, m)
        # left
        func(x + 3*m, y - 3*m, m)
        # right
        func(x + 3*m, y + 3*m, m)

func(0, (5*N//3 + N//3 - 1) // 2, N//3)

# 출력 최적화: 각 행을 문자열로 변환한 후 출력
for row in stars:
    print(''.join(row))